;; Intro to Lists
;; Difficulty:	Elementary
;; Topics:

;; Lists can be constructed with either a function or a quoted form.

;; (= (list __) '(:a :b :c))

;; Answer:
;; :a :b :c

;; Code:
(= (list :a :b :c) '(:a :b :c))

(comment
  Explanation
  There are two ways to construct a list and they are equivalent.
)
